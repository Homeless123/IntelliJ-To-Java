package day16_nestedLoop;

public class NestedLoodIntro {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("Besiktas");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Besiktas");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Besiktas");
        }

        for (int i = 0; i < 5; i++) {
            System.out.println("Besiktas");
        }
        for (int i = 0; i < 5; i++) {
            System.out.println("Besiktas");
        }
        System.out.println("------------------------");

        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                System.out.println("hello java");
            }
        }


    }
}
